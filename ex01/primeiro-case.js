var x = parseFloat(prompt('Digite um número entre 1 e 7 para saber o dia de semana'));
        switch (x) {
            case 1:
                alert('Hoje é Domingo')

                break;
            case 2:
                alert('Hoje é Segunda-feira')

                break;
            case 3:
                alert('Hoje é Terça-Feira')

                break;
            case 4:
                alert('Hoje é Quarta-Feira')

                break;
            case 5:
                alert('Hoje é Quinta-Feira')

                break;
            case 6:
                alert('Hoje é Sexta-Feira')

                break;
            case 7:
                alert('Hoje é Sábado')

                break;
        }
